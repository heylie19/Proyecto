    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fila=tabla.getSelectedRow();
        if(fila>=0)
        {
            txtBuscar.setText(tabla.getValueAt(fila, 1).toString());
            txtAlumno.setText(tabla.getValueAt(fila, 1).toString());
            txtApellido.setText(tabla.getValueAt(fila, 2).toString());
            txtEmail.setText(tabla.getValueAt(fila, 3).toString());
            txtNombre.setText(tabla.getValueAt(fila, 4).toString());
            txtTel.setText(tabla.getValueAt(fila, 5).toString());
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Fila no Seleccionada");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            PreparedStatement pps=cn.prepareStatement("UPDATE contact_list SET Alumno='"+txtAlumno.getText()+"', Apellido='"+txtApellido.getText()+"', Email='"+txtEmail.getText()+"', Nombre='"+txtNombre.getText()+"', Telefono='"+txtTel.getText()+"' WHERE Alumno='"+txtAlumno.getText()+"'");
        pps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Datos Actualizados");
        limpiar();
        Lista();
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(frmPrincipal.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        // TODO add your handling code here:
        
        int fila=tabla.getSelectedRow();
        String valor=tabla.getValueAt(fila,1).toString();
        if(fila>=0)
        {
            try {
                PreparedStatement pps=cn.prepareStatement("DELETE FROM contact_list WHERE Alumno='"+valor+"'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos Eliminados");
                Lista();
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(frmPrincipal.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminar1ActionPerformed
