    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            PreparedStatement pps=cn.prepareStatement("INSERT INTO contact_list(Alumno,Apellido,Email,Nombre,Telefono) VALUES(? ,? ,? ,? ,? )");
            pps.setString(1, txtAlumno.getText());
            pps.setString(2, txtApellido.getText());
            pps.setString(3, txtEmail.getText());
            pps.setString(4, txtNombre.getText());
            pps.setString(5, txtTel.getText());
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Guardados");
            
        }catch(SQLException ex)
        {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

